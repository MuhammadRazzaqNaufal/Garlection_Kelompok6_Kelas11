        <h2>
          Banda Aceh, mengelola sampah untuk menghijaukan kota
        </h2>
        <p>
          Mengenakan kemeja merah, seorang pria paruh baya berdiri tegak melihat ke arah jalan tak beraspal 
          di Tempat komplek Pemrosesan Akhir (TPA) di Gampong Jawa Kecamatan Kutaraja Banda Aceh. 
          Pria bernama Darma (50) itu sedang menunggu truk-truk besar pengangkut sampah organik kembali 
          dari pasar tradisional dan pemukiman warga di Ibu Kota Provinsi Aceh ini.
        </p>
        <p>
          Tepat pukul 16.00 WIB, belum ada mobil yang kembali, lalu ia bergerak menuju kotak besi besar 
          tempat penyimpanan sampah organik atau yang dikenal dengan nama hidrolisis. 
        </p>
        <p>
          Di TPA Gampong Jawa Banda Aceh terdapat 14 hidrolisis, tempat tersebut digunakan untuk 
          pembusukan sampah organik yang dikutip dari pasar dan perumahan masyarakat, kemudian 
          disulap menjadi pupuk kompos. Langit mendung, hujan pun mulai mengguyur area TPA 
          tepatnya di lokasi Intermediate Treatment Facility (ITF) atau tempat pengolahan sampah kompos 
          dan biogas milik Dinas Lingkungan Hidup, Kebersihan dan Kebersihan Kota (DLHK3) Banda Aceh itu.
        </p>
        <p>
          TPA tepatnya di lokasi Intermediate Treatment Facility (ITF) atau tempat pengolahan sampah kompos 
          dan biogas milik Dinas Lingkungan Hidup, Kebersihan dan Keindahan Kota (DLHK3) Banda Aceh itu. 
          
        </p>
        <p>
          Bergegas Darma mengarahkan ke satu tempat istirahat agar tak terkena hujan, di sana ia menceritakan 
          bagaimana pengelolaan sampah yang dilaksanakan di ITF tersebut, terutama terhadap sampah yang diolah 
          menjadi pupuk kompos dan lainnya.ITF tersebut, terutama terhadap sampah yang diolah menjadi pupuk kompos 
          dan lainnya.
        </p>
        <p>
          Sebagai pengelola ITF, Darma menjelaskan bahwa selama ini pihaknya mengelola sampah organik yang berasal 
          dari TPA dan juga diambil langsung pada pasar tradisional terpadu Al Mahirah Lamdingin Banda Aceh.
          Sampah organik setiap hari dikumpulkan dari pasar Al Mahirah biasanya mencapai sekitar 500 kilogram (kg) per hari. 
          Kemudian, hasil pemilahan sampah rumah tangga yang dikumpulkan pada TPA didapatkan sekitar 250 kg per hari.  
        </p>
        <p>
          Sampah yang telah dipilah tersebut kemudian dimasukkan ke dalam hidrolisis untuk pembusukan, pada satu hidrolisis 
          itu memuat sekitar 700 kg sampai 900 kg. "Untuk setiap hidrolisis kita masukan sekitar 750 kg atau 900 kg. Jumlah ini 
          kita batasi agar lebih mudah diproses, karena kalau terlalu banyak susah kita lakukan pembalikannya," kata Darma.
        </p>
        <p>
          Setelah masuk ke dalam hidrolisis, selanjutnya dilakukan penyiraman dan pembalikan selama dua sampai 3 hari sekali. 
          Proses ini bisa menghabiskan waktu hingga satu bulan. Setelah terjadinya pembusukan pada sampah dalam hidrolisis tersebut, kata Darma, 
          baru dikeluarkan untuk kemudian masuk tahap penjemuran untuk mengurangi debit air pada sampah sampai betul-betul kering, 
          sehingga benar-benar memudahkan proses penggilingan. Setelah penggilingan, baru kemudian dilakukan tahapan pengayakan, 
          untuk memisahkan ampas dengan sarinya atau pupuk kompos yang dihasilkan. "Pupuk kompos yang dihasilkan kalau dari 900 kg sampah 
          organik itu tidak banyak, hanya sekitar 200 kg, hal itu karena memang volume penyusutan nya cukup tinggi," ujar Darma.
        </p>
        <p>
          Kepala DLHK3 Banda Aceh Hamdani Basyah melalui Kabid Pengelolaan Sampah dan Limbah B3 Asnawi mengatakan, 
          pembuatan pupuk kompos selama ini dihasilkan dari pengelolaan di ITF Gampong Jawa dan juga di kawasan Ilie Banda Aceh. 
          Di mana, pupuk kompos tersebut selama ini tidak ada yang dikomersilkan, melainkan dimanfaatkan untuk memenuhi kebutuhan 
          penghijauan ibu kota provinsi Aceh itu. "Jadi pupuk kompos tersebut dimanfaatkan untuk penghijauan taman-taman kota, 
          serta pada pepohonan atau tanaman yang ada di wilayah jalanan perkotaan di Banda Aceh," kata Asnawi.
        </p>
        <p>
          Selama ini, pemerintah Banda Aceh tidak harus membeli lagi pupuk untuk menghijaukan kota, karena memang produksi kompos 
          lebih kurang mencapai 200 kg per hari, dan memenuhi kebutuhan. "Tidak kita jual belikan karena memang pupuk kompos yang kita 
          hasilkan ini terbatas, hanya cukup untuk memenuhi kebutuhan tanaman di kota saja," ujar Asnawi. Hanya saja, dengan adanya pupuk dari ITF tersebut, 
          minimal pemerintah tidak harus lagi mengeluarkan anggaran daerah, dengan demikian bisa sedikit menghemat pada program penghijauan kota, 
          sehingga bisa dialihkan untuk kebutuhan lainnya.
        </p>
