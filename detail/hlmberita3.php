        <h2>
          Produksi Sampah Banda Aceh Capai 240 Ton Per Hari
        </h2>
        <p>
          DLHK3 Banda Aceh mencatat, sampah yang dikumpulkan per hari di kota tersebut 
          lebih kurang mencapai 24 ton per hari baik itu organik maupun an organik. 
          Angka tersebut hanya pada hari normal. Namun, sampah bisa lebih tinggi saat 
          pelaksanaan hari-hari besar Aceh, seperti datangnya tradisi meugang menyambut ramadhan, 
          hari raya Idul Fitri dan Idul Adha, itu bisa mencapai 250 ton per hari. 
        </p>
        <p>
          "Kalau ada hari besar seperti hari raya, sampah di pasar sudah pasti meningkat, 
          kemudian rumah tangga juga karena memang masyarakat lebih banyak kebutuhan," kata Asnawi. 
          Secara tahunan, produksi sampah pada 2022 sekitar 91 ribu ton lebih, 
          angka tersebut sedikit menurun jika dibandingkan pada 2021 lalu yang mencapai 142 ribu ton. 
          Lalu, untuk 2023 ini yaitu pada Januari mencapai 7,5 ribu ton. "Tahun 2022 itu sampah organiknya 51,131 ton 
          dan non organik 40 ribu ton. Sedangkan untuk 2023 organik 4,2 ribu, dan an organik 3,3 ribu ton," ujarnya.
        </p>
        <p>
          Asnawi menjelaskan, petugas kebersihan setiap harinya bertugas mengumpulkan sampah itu dimulai pukul 06.00 WIB 
          sampai malam dengan pembagian tiga shift. Mereka, mengumpulkan sampah pada titik lokasi mulai dari perkampungan, 
          jalan protokol, dan pasar tradisional. Setelah itu semuanya dibawa ke TPA Gampong Jawa, dikelola menjadi 
          pupuk kompos hingga biogas.  Dirinya menyampaikan, sampah memang tidak semuanya dikumpulkan dan dibawa ke TPA, 
          tetapi juga ada sampah yang dikumpulkan oleh kelompok masyarakat untuk kemudian dijual guna didaur ulang.
        </p>
        <p>
          "Setelah dikumpulkan masyarakat, nanti saat waktunya kita angkut sampah itu untuk didaur ulang, 
          kemudian itu dicatat ke buku bank sampah, sehingga bisa menghasilkan uang kepada kelompok masyarakat tersebut," katanya. 
          Kemudian, di Banda Aceh saat ini juga ada masyarakat yang mengolah sampah menjadi bahan souvenir, 
          tergantung dari kreativitas masyarakat. Bisa diolah menjadi barang seperti dompet, tas, keranjang 
          dan lain sebagainya. "Hasil karya mereka yang juga di bawah binaan kita itu kemudian kita beli, sehingga 
          mereka terus bersemangat mengolah sampah itu menjadi souvenir," ujarnya. Tak hanya untuk pupuk kompos, bank sampah 
          dan souvenir. Sampah Banda Aceh yang dikumpulkan ke TPA juga dimanfaatkan menjadi bahan bakar yaitu biogas. 
        </p>
        <p>
          Sejauh ini, DLHK3 Banda Aceh sudah menyalurkan biogas dari sampah itu ke 210 KK masyarakat di Gampong Jawa. 
          Namun, saat ini sudah berhenti karena ada pipa yang bocor sehingga gas tidak mengalir lagi. 
          Kata Asnawi, biogas di TPA tersebut masih tersedia, hanya saja perlu dilakukan perbaikan jaringan 
          supaya gas tersebut bisa kembali dialirkan ke pemukiman masyarakat. 
        </p>
        <p>
          "Insya Allah setelah APBD Banda Aceh sudah membaik, kita akan kembali usulkan pengadaan jaringan baru. 
          Sehingga biogas ini bisa kembali dinikmati masyarakat," demikian Asnawi.
        </p>
